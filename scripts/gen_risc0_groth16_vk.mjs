function u256beFromDec(dec) {
  const n = BigInt(dec);
  const out = new Uint8Array(32);
  let v = n;
  for (let i = 31; i >= 0; i--) {
    out[i] = Number(v & 0xffn);
    v >>= 8n;
  }
  return out;
}

function g1Bytes(xDec, yDec) {
  const x = u256beFromDec(xDec);
  const y = u256beFromDec(yDec);
  const out = new Uint8Array(64);
  out.set(x, 0);
  out.set(y, 32);
  return out;
}

// CAP-0074 G2 encoding: X_c1||X_c0||Y_c1||Y_c0 (each limb 32 bytes BE).
function g2Bytes(x1Dec, x2Dec, y1Dec, y2Dec) {
  const x1 = u256beFromDec(x1Dec);
  const x2 = u256beFromDec(x2Dec);
  const y1 = u256beFromDec(y1Dec);
  const y2 = u256beFromDec(y2Dec);
  const out = new Uint8Array(128);
  out.set(x2, 0); // X_c1
  out.set(x1, 32); // X_c0
  out.set(y2, 64); // Y_c1
  out.set(y1, 96); // Y_c0
  return out;
}

function toRustU8Array(name, bytes) {
  const items = Array.from(bytes).map((b) => `0x${b.toString(16).padStart(2, "0")}`);
  return `pub const ${name}: [u8; ${bytes.length}] = [\n  ${items.join(", ")}\n];\n`;
}

